    private void btnPriorityBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPriorityBookActionPerformed
        // TODO add your handling code here:
        String patients = "patients";
        this.listaLeida = operacionArchivo.leerArchivo(patients);
        int c = 0;
        Ordenar.selectionSort(listaLeida);
        
        for (int i = 0; i < listaLeida.size(); i++) {
            c++;
            try {
                Patient patient = new Patient();
                patient = listaLeida.getAt(i);
                Panel.append("Paciente " + c + ":\n" + patient.getId() + "  " + patient.getName() + "   " + patient.getPriority() + "\n");
            } catch (Exception ex) {
                Logger.getLogger(Main_urgencias.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        patients = "internados";
        this.listaLeida = operacionArchivo.leerArchivo(patients);
        c = 0;
       // Ordenar.selectionSort(listaLeida);
        for (int i = 0; i < listaLeida.size(); i++) {
            c++;
            try {
                Internados inter = new Internados();
                inter = listaLeida.getAtInternados(i);
                txaInternados.append(inter.getDatos() + "  " + inter.getPrio() + "\n");
            } catch (Exception ex) {
                Logger.getLogger(Main_urgencias.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnPriorityBookActionPerformed

