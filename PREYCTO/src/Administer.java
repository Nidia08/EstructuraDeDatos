    private void btnDocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDocActionPerformed
        // TODO add your handling code here:
        JPanel panel = new JPanel();
        JTextField una = new JTextField(12);
        JTextField pues = new JTextField(12);
        panel.add(new JLabel("Usuario:"));
        panel.add(una);
        panel.add(new JLabel("Puesto:"));
        panel.add(pues);
        int val = JOptionPane.showConfirmDialog(this, panel);

        regDoc = new DocEnf();

        if (pues.getText().equals("Doctor")) {
            regDoc.setPsw(una.getText() + "3");
        } else if (pues.getText().equals("Enfermero")) {
            regDoc.setPsw(una.getText() + "4");
        }
        regDoc.setdId(una.getText());
        regDoc.setPuesto(pues.getText());
        if (val == 0) {
            myList.addBegin(new Nodo(regDoc));
            String docEnf = "docEnf";
            operacionArchivo.addArchivo(myList,docEnf);
            JOptionPane.showMessageDialog(null, "Operaci√≥n realizada correctamente");
        }
    }//GEN-LAST:event_btnDocActionPerformed

