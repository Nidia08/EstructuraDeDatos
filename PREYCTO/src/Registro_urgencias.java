    private void btnEnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnterActionPerformed
        // TODO add your handling code here:
        
        c++;

        if (txtPriority.getText().equals("3")) {
            Internados inter = new Internados();
            String datos = "Paciente : " + txtID.getText() + " " + txtName.getText();
            inter.setDatos(datos);
            inter.setObservacion("Observaci√≥n: Pendiente");
            inter.setEnf("Enfermedad: Pendiente");
            inter.setCuar("Estado: " + c);
            inter.setPrio(txtPriority.getText());
            listaUrg.addBegin(new Nodo(inter));
            String persona = "internados";
            operacionArchivo.addArchivo(listaUrg, persona);
            txtPriority.setText("");
            txtID.setText("");
            txtName.setText("");
        } else {
            Patient patient = new Patient();
            patient.setId(txtID.getText());
            patient.setName(txtName.getText());
            patient.setPriority(txtPriority.getText());
            listaPaciente.addBegin(new Nodo(patient));
            String patients = "patients";
            operacionArchivo.addArchivo(listaPaciente, patients);
            txtPriority.setText("");
            txtID.setText("");
            txtName.setText("");
        }


    }//GEN-LAST:event_btnEnterActionPerformed

